package week1day2;

public class PositiveOrNegative {

	public static void main(String[] args) {
		int input = 0;
		
		System.out.println("Selenium Assignment Week 1 Day 2 Assignment 1 - PositiveOrNegative"+'\n');
		
		if (input >0) {
			System.out.println("Given Number " + input + " is a positive number");
		}
		else if (input <0) {
			System.out.println("Given Number " + input + " is a negative number");
		}
		else {
			System.out.println("Given Number " + input + " is neither a positive or negative");
		}

	}

}
